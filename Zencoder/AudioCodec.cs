

namespace Zencoder
{
    using System;

    /// <summary>
    /// Defines the possible output audio codecs.
    /// </summary>
    public enum AudioCodec
    {
        /// <summary>
        /// Identifies the AAC audio codec.
        /// </summary>
        Aac = 0,

        /// <summary>
        /// Identifies that MP3 audio codec.
        /// </summary>
        Mp3,

        /// <summary>
        /// Identifies the Vorbis audio codec.
        /// </summary>
        Vorbis
    }
}
